#include "wolf.h"

void		clear_image(t_mlx *mlx)
{
	int		i;
	int		j;

	i = -1;
	while (++i < SCREEN_SIZE)
	{
		j = -1;
		while (++j < SCREEN_SIZE)
			put_pixel(mlx, i, j, 0x00000000);
	}
}
